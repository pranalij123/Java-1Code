class PrintLongValue {
    public static void main(String [] args)
    {
         char grade='A';
        switch(grade){
            case 'A':
            System.out.println("marks is within 80 to 100");
            break;

            case 'B':
            System.out.println("marks is within 60 to 80");
            break;
            case 'C':
            System.out.println("marks is within 40 to 60");
            break;
            case 'E':
            System.out.println("marks is within 35 to 40");
            break;
            case 'F':
            System.out.println("marks is within 0 to 35");
            break;


        }
    }
}


